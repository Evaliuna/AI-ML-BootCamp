print(bool(0))          #Will give false output because the number 0 is always considered empty or false.
print(bool(''))         #output false because an empty text string has no value inside it.
print(bool('hello'))    #output true because any text string with characters in it is true.
print(bool(99))         #output true because non-zero number is always considered true.
print(int(True))        #output 1 because true define by 1 in python.
print(int(False))       #output 0 because false define by 0 in python.
print(float('3.14'))    #output 3.14 because it stays in its same form foat.