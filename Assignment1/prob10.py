"""
i have taken a string and list then check
if a specific string belong to string or a
specific number belong to the list or not using
'in' and 'not in'.
"""
str_trgt = 'python'
li_trgt = [1,3,5,7,9]

print("Is 'py' inside 'python'?", 'py' in str_trgt)
print("Is 'z' not in 'python'?", 'z' not in str_trgt)
print("Is 7 in the list?", 7 in li_trgt)
print("Is 4 not in the list?", 4 not in li_trgt)